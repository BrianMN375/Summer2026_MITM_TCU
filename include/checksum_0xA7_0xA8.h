// =============================================================================
//  checksum_0xA7_0xA8.h — VW MQB CAN checksum for IDs 0x0A7 and 0x0A8
//
//  Validated: 162,042 / 162,042 frames (0xA7)  100.000%
//             162,060 / 162,060 frames (0xA8)  100.000%
//
//  Algorithm:
//      b0 = T_b2[b2] ^ T_b3[b3] ^ T_b4[b4] ^ T_b5[b5] ^ T_b6[b6] ^ T_b7[b7]
//           ^ OFFSET ^ G_HI[b1 >> 4] ^ G_LO[b1 & 0x0F]
//
//  Where:
//    T_bj[]  – six 256-entry PROGMEM lookup tables (one per payload byte b2–b7).
//              Derived from a GF(2) linear map; within each byte the bit effects
//              follow a reflected CRC-8 LFSR chain (poly=0x97).
//    G_HI[]  – 16-entry table: linear contribution of b1 upper nibble (shared).
//    G_LO[]  – 16-entry table: non-linear rolling-counter contribution from b1
//              lower nibble. Differs between 0xA7 and 0xA8.
//    OFFSET  – per-ID constant.
//
//  API (matches existing per-ID style in checksum.h):
//
//    // Compute only — payload7[0..6] = b1..b7, result -> *byte0_out
//    bool checksum_update_byte0_0xA7(const uint8_t payload7[7], uint8_t *byte0_out);
//    bool checksum_update_byte0_0xA8(const uint8_t payload7[7], uint8_t *byte0_out);
//
//    // Build-frame helpers — fill buf[0] in place, always return true
//    bool build_frame_with_checksum_0xA7(uint8_t buf[8]);
//    bool build_frame_with_checksum_0xA8(uint8_t buf[8]);
//
//  Typical MITM usage:
//    // 1. Receive frame from CAN bus
//    CAN_message_t msg = <received>;
//    // 2. Modify payload bytes as needed (buf[1..7])
//    msg.buf[3] = new_value;
//    // 3. Recompute checksum and inject into buf[0]
//    build_frame_with_checksum_0xA7(msg.buf);   // for 0xA7
//    // 4. Forward the modified frame
//    CAN2.write(msg);
// =============================================================================

